export function Sidebar() {
  const pathname = usePathname()
  const { user, signOut } = useAuth()
  const { isAdmin } = useUserRole()
  const [mobileOpen, setMobileOpen] = useState(false)

  // El item Admin solo se renderiza si el usuario es admin. Es defensa en
  // profundidad solamente — la API ya valida via SECURITY DEFINER.
  const items = isAdmin ? [...navItems, adminNavItem] : navItems

  const nav = (
    <div className="flex flex-col h-full bg-zinc-900 border-r border-zinc-800">
      <div className="px-5 py-5 flex items-center gap-3">
        {/* Logo Sales Voice — diamante + S neón rojo */}
        <img src="/icon.svg" alt="Sales Voice" className="h-9 w-9 shrink-0" />
        <div>
          <h1 className="text-base font-bold text-white tracking-tight leading-none">Sales Voice</h1>
          <p className="text-xs text-zinc-500 mt-0.5">Roleplay de ventas IA</p>
        </div>
      </div>

      <nav className="flex-1 px-3 space-y-1">
        {items.map((item) => {
          const isActive = pathname === item.href || pathname.startsWith(item.href + '/')
          return (
            <Link
              key={item.href}
              href={item.href}
              onClick={() => setMobileOpen(false)}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                isActive
                  ? 'bg-red-600/20 text-red-400 border-l-2 border-red-500'
                  : 'text-zinc-400 hover:text-white hover:bg-zinc-800/50'
              }`}
            >
              {item.icon}
              {item.label}
            </Link>
          )
        })}
      </nav>

      <div className="p-4 border-t border-zinc-800">
        <div className="flex items-center gap-3">
          <div className="h-8 w-8 rounded-full bg-zinc-700 flex items-center justify-center text-xs font-medium text-zinc-300">
            {user?.user_metadata?.name?.[0]?.toUpperCase() || user?.email?.[0]?.toUpperCase() || '?'}
          </div>
          <div className="flex-1 min-w-0">
            <p className="text-sm font-medium text-white truncate">
              {user?.user_metadata?.name || 'Usuario'}
            </p>
            <p className="text-xs text-zinc-500 truncate">{user?.email}</p>
          </div>
          <button
            onClick={signOut}
            className="text-zinc-500 hover:text-white transition-colors"
            title="Cerrar sesión"
          >
            <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
              <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 9V5.25A2.25 2.25 0 0013.5 3h-6a2.25 2.25 0 00-2.25 2.25v13.5A2.25 2.25 0 007.5 21h6a2.25 2.25 0 002.25-2.25V15m3 0l3-3m0 0l-3-3m3 3H9" />
            </svg>
          </button>
        </div>
      </div>
    </div>
  )

  return (
    <>
      <button
        onClick={() => setMobileOpen(true)}
        className="lg:hidden fixed top-4 left-4 z-50 p-2 rounded-lg bg-zinc-900 border border-zinc-800 text-zinc-400 hover:text-white"
      >
        <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
          <path strokeLinecap="round" strokeLinejoin="round" d="M3.75 6.75h16.5M3.75 12h16.5m-16.5 5.25h16.5" />
        </svg>
      </button>

      {mobileOpen && (
        <div className="lg:hidden fixed inset-0 z-40">
          <div className="absolute inset-0 bg-black/60" onClick={() => setMobileOpen(false)} />
          <div className="relative w-64 h-full">{nav}</div>
        </div>
      )}

      <div className="hidden lg:block w-64 shrink-0">{nav}</div>
    </>
  )
}
